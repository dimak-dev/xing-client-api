/**
 * Enumeration for different status types.
 */
export enum EXingOrderStatus {
    /**
     * Represents an active state.
     */
    ACTIVE = 'active',

    /**
     * Represents a state that has not been started.
     */
    UNSTARTED = 'unstarted',

    /**
     * Represents an expired state.
     */
    EXPIRED = 'expired',
}
